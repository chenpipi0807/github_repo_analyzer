class GitHubRepoAnalyzer:
    def __init__(self, repo_url, lang='en'):
        """Initialize with GitHub repository URL"""
        self.repo_url = repo_url
        self.owner, self.repo = self._parse_url(repo_url)
        self.base_api = f"https://api.github.com/repos/{self.owner}/{self.repo}"
        self.raw_base = f"https://raw.githubusercontent.com/{self.owner}/{self.repo}"
        self.data = {}
        self.lang = lang
        self.t = I18N[lang]
        
    def _parse_url(self, url):
        """Parse GitHub URL to extract owner and repo"""
        parsed = urlparse(url)
        path_parts = parsed.path.strip('/').split('/')
        if len(path_parts) >= 2:
            return path_parts[0], path_parts[1]
        raise ValueError("Invalid GitHub URL")
    
    def fetch_all_data(self):
        """Fetch all repository data from GitHub API"""
        print(self.t['analyzing'].format(owner=self.owner, repo=self.repo))
        
        # Basic repo info
        self.data['basic'] = self._fetch_json(self.base_api)
        
        # Contributors
        self.data['contributors'] = self._fetch_json(f"{self.base_api}/contributors?per_page=100")
        
        # Recent commits (for activity analysis)
        since = (datetime.now() - timedelta(days=90)).isoformat()
        self.data['commits'] = self._fetch_json(f"{self.base_api}/commits?since={since}&per_page=100")
        
        # Issues
        self.data['issues'] = self._fetch_json(f"{self.base_api}/issues?state=all&per_page=100")
        
        # Pull requests
        self.data['pulls'] = self._fetch_json(f"{self.base_api}/pulls?state=all&per_page=100")
        
        print(self.t['data_fetched'])
        return self.data
    
    def _fetch_json(self, url):
        """Fetch JSON data from GitHub API"""
        try:
            response = requests.get(url, timeout=10)
            if response.status_code == 200:
                return response.json()
            else:
                print(self.t['warning'].format(url=url, status=response.status_code))
                return []
        except Exception as e:
            print(self.t['error_fetch'].format(url=url, error=e))
            return []
    
    def analyze_basic_stats(self):
        """Analyze basic repository statistics"""
        basic = self.data.get('basic', {})
        return {
            'name': basic.get('name', 'Unknown'),
            'full_name': basic.get('full_name', f'{self.owner}/{self.repo}'),
            'description': basic.get('description', 'No description') or 'No description',
            'stars': basic.get('stargazers_count', 0),
            'forks': basic.get('forks_count', 0),
            'watchers': basic.get('watchers_count', 0),
            'open_issues': basic.get('open_issues_count', 0),
            'language': basic.get('language', self.t['unknown']),
            'created_at': basic.get('created_at', ''),
            'updated_at': basic.get('updated_at', ''),
            'size': basic.get('size', 0),
            'license': (basic.get('license') or {}).get('name', self.t['no_license']),
            'topics': basic.get('topics', []),
        }
    
    def analyze_contributors(self):
        """Analyze contributor activity"""
        contributors = self.data.get('contributors', [])
        if not contributors:
            return {'total': 0, 'top_contributors': [], 'avg_contributions': 0, 'total_contributions': 0}
        
        total_contributions = sum(c.get('contributions', 0) for c in contributors)
        top_contributors = [
            {
                'login': c.get('login', 'Unknown'),
                'contributions': c.get('contributions', 0)
            }
            for c in sorted(contributors, key=lambda x: x.get('contributions', 0), reverse=True)[:10]
        ]
        
        return {
            'total': len(contributors),
            'top_contributors': top_contributors,
            'avg_contributions': total_contributions / len(contributors),
            'total_contributions': total_contributions,
        }
    
    def analyze_commits(self):
        """Analyze commit activity"""
        commits = self.data.get('commits', [])
        commit_dates = []
        authors = set()
        
        for commit in commits:
            try:
                date_str = commit.get('commit', {}).get('author', {}).get('date', '')
                if date_str:
                    commit_dates.append(datetime.fromisoformat(date_str.replace('Z', '+00:00')))
                author = commit.get('author', {}).get('login', '') if commit.get('author') else None
                if author:
                    authors.add(author)
            except:
                continue
        
        return {
            'total_recent': len(commits),
            'commit_dates': commit_dates,
            'unique_authors': len(authors),
        }
    
    def analyze_issues(self):
        """Analyze issue statistics"""
        issues = self.data.get('issues', [])
        open_issues = [i for i in issues if i.get('state') == 'open' and 'pull_request' not in i]
        closed_issues = [i for i in issues if i.get('state') == 'closed' and 'pull_request' not in i]
        
        # Calculate average resolution time
        resolution_times = []
        for issue in closed_issues:
            created = issue.get('created_at')
            closed = issue.get('closed_at')
            if created and closed:
                try:
                    created_dt = datetime.fromisoformat(created.replace('Z', '+00:00'))
                    closed_dt = datetime.fromisoformat(closed.replace('Z', '+00:00'))
                    resolution_times.append((closed_dt - created_dt).days)
                except:
                    continue
        
        avg_resolution = sum(resolution_times) / len(resolution_times) if resolution_times else 0
        
        return {
            'total': len(open_issues) + len(closed_issues),
            'open': len(open_issues),
            'closed': len(closed_issues),
            'resolution_rate': len(closed_issues) / (len(open_issues) + len(closed_issues)) if (open_issues or closed_issues) else 0,
            'avg_resolution_time': avg_resolution,
        }
    
    def analyze_pull_requests(self):
        """Analyze pull request statistics"""
        pulls = self.data.get('pulls', [])
        
        open_prs = [p for p in pulls if p.get('state') == 'open']
        closed_prs = [p for p in pulls if p.get('state') == 'closed']
        merged_prs = [p for p in closed_prs if p.get('merged_at')]
        
        return {
            'total': len(pulls),
            'open': len(open_prs),
            'merged': len(merged_prs),
            'closed': len(closed_prs) - len(merged_prs),
            'merge_rate': len(merged_prs) / (len(merged_prs) + len(closed_prs) - len(merged_prs)) if closed_prs else 0,
        }
    
    def calculate_health_score(self):
        """Calculate overall repository health score"""
        basic = self.analyze_basic_stats()
        contributors = self.analyze_contributors()
        issues = self.analyze_issues()
        pulls = self.analyze_pull_requests()
        commits = self.analyze_commits()
        
        score = 0
        factors = []
        
        # Stars popularity (max 20 points)
        stars_score = min(20, basic['stars'] / 100)
        score += stars_score
        factors.append((self.t['stars_popularity'], stars_score, 20))
        
        # Contributor diversity (max 20 points)
        contrib_score = min(20, contributors['total'] * 2)
        score += contrib_score
        factors.append((self.t['contributor_diversity'], contrib_score, 20))
        
        # Issue resolution (max 20 points)
        issue_score = 20 * issues['resolution_rate'] if issues['total'] > 0 else 10
        score += issue_score
        factors.append((self.t['issue_resolution'], issue_score, 20))
        
        # PR merge rate (max 20 points)
        pr_score = 20 * pulls['merge_rate'] if pulls['total'] > 0 else 10
        score += pr_score
        factors.append((self.t['pr_merge_rate'], pr_score, 20))
        
        # Recent activity (max 20 points)
        activity_score = min(20, commits['total_recent'] / 5)
        score += activity_score
        factors.append((self.t['recent_activity_factor'], activity_score, 20))
        
        return {
            'total_score': round(score, 1),
            'grade': self._get_grade(score),
            'factors': factors,
        }
    
    def _get_grade(self, score):
        """Convert score to letter grade"""
        if score >= 90: return self.t['grade_A+']
        if score >= 80: return self.t['grade_A']
        if score >= 70: return self.t['grade_B+']
        if score >= 60: return self.t['grade_B']
        if score >= 50: return self.t['grade_C']
        if score >= 40: return self.t['grade_D']
        return self.t['grade_F']
    
    def generate_visualizations(self, output_dir='./output'):
        """Generate visualization charts"""
        os.makedirs(output_dir, exist_ok=True)
        
        # Create figure with subplots
        fig = plt.figure(figsize=(16, 10))
        fig.suptitle(f"{self.t['repo_health_report']}: {self.owner}/{self.repo}", fontsize=16, fontweight='bold')
        
        # 1. Basic Stats Pie Chart
        ax1 = plt.subplot(2, 3, 1)
        basic = self.analyze_basic_stats()
        stats_data = [basic['stars'], basic['forks'], basic['watchers']]
        colors = ['#FFD700', '#4169E1', '#32CD32']
        ax1.pie(stats_data, labels=[f"{self.t['stars']}\n{basic['stars']}", f"{self.t['forks']}\n{basic['forks']}", 
                                   f"{self.t['watchers']}\n{basic['watchers']}"], 
               colors=colors, autopct='%1.1f%%', startangle=90)
        ax1.set_title(self.t['repo_popularity'])
        
        # 2. Top Contributors Bar Chart
        ax2 = plt.subplot(2, 3, 2)
        contributors = self.analyze_contributors()
        if contributors['top_contributors']:
            logins = [c['login'][:10] for c in contributors['top_contributors'][:5]]
            contribs = [c['contributions'] for c in contributors['top_contributors'][:5]]
            ax2.barh(logins, contribs, color='#FF6B6B')
            ax2.set_xlabel(self.t['contributions'])
            ax2.set_title(self.t['top_contributors'])
            ax2.invert_yaxis()
        
        # 3. Health Score Gauge
        ax3 = plt.subplot(2, 3, 3)
        health = self.calculate_health_score()
        score = health['total_score']
        color = '#2ECC71' if score >= 70 else '#F39C12' if score >= 50 else '#E74C3C'
        ax3.barh([0], [score], color=color, height=0.5)
        ax3.set_xlim(0, 100)
        ax3.set_yticks([0])
        ax3.set_yticklabels([self.t['health_score']])
        ax3.set_xlabel(self.t['score'])
        ax3.set_title(f"{self.t['overall_health']}: {score}/100 - {health['grade']}")
        ax3.axvline(x=60, color='red', linestyle='--', alpha=0.5, label=self.t['pass_line'])
        
        # 4. Issue Status
        ax4 = plt.subplot(2, 3, 4)
        issues = self.analyze_issues()
        if issues['total'] > 0:
            issue_data = [issues['open'], issues['closed']]
            ax4.pie(issue_data, labels=[f"{self.t['open']}\n{issues['open']}", f"{self.t['closed']}\n{issues['closed']}"],
                   colors=['#E74C3C', '#2ECC71'], autopct='%1.1f%%')
            ax4.set_title(self.t['issue_status'].format(days=issues['avg_resolution_time']))
        
        # 5. PR Status
        ax5 = plt.subplot(2, 3, 5)
        pulls = self.analyze_pull_requests()
        if pulls['total'] > 0:
            pr_data = [pulls['open'], pulls['merged'], pulls['closed']]
            ax5.pie(pr_data, labels=[f"{self.t['open']}\n{pulls['open']}", f"{self.t['merged']}\n{pulls['merged']}", 
                                    f"{self.t['closed']}\n{pulls['closed']}"],
                   colors=['#F39C12', '#2ECC71', '#E74C3C'], autopct='%1.1f%%')
            ax5.set_title(self.t['pr_status'].format(rate=pulls['merge_rate']*100))
        
        # 6. Commit Activity Timeline
        ax6 = plt.subplot(2, 3, 6)
        commits = self.analyze_commits()
        if commits['commit_dates']:
            dates = commits['commit_dates']
            dates.sort()
            date_counts = Counter([d.date() for d in dates])
            x = list(date_counts.keys())
            y = list(date_counts.values())
            ax6.plot(x, y, color='#9B59B6', linewidth=2)
            ax6.fill_between(x, y, alpha=0.3, color='#9B59B6')
            ax6.set_xlabel(self.t['date'])
            ax6.set_ylabel(self.t['commits'])
            ax6.set_title(self.t['recent_activity'])
            ax6.tick_params(axis='x', rotation=45)
        
        plt.tight_layout()
        
        # Save figure
        chart_path = os.path.join(output_dir, f'{self.repo}_analysis.png')
        plt.savefig(chart_path, dpi=150, bbox_inches='tight')
        print(self.t['chart_saved'].format(path=chart_path))
        plt.close()
        
        return chart_path
    
