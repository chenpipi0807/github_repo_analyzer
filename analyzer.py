def main():
    """Main entry point"""
    import argparse
    
    parser = argparse.ArgumentParser(
        description='GitHub Repository Health Analyzer / GitHub仓库健康度分析器',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog='''
Examples:
  python analyzer.py https://github.com/torvalds/linux
  python analyzer.py https://github.com/torvalds/linux --lang zh
  python analyzer.py https://github.com/microsoft/vscode --output ./reports --lang zh
        '''
    )
    parser.add_argument('url', help='GitHub repository URL / GitHub 仓库地址')
    parser.add_argument('--output', '-o', default='./output', 
                       help='Output directory (default: ./output) / 输出目录 (默认: ./output)')
    parser.add_argument('--format', '-f', choices=['html', 'summary', 'json', 'all'], 
                       default='all', help='Output format / 输出格式')
    parser.add_argument('--lang', '-l', choices=['en', 'zh'], 
                       default='en', help='Language / 语言 (en: 英文, zh: 中文)')
    
    args = parser.parse_args()
    
    # Run analysis
    analyzer = GitHubRepoAnalyzer(args.url, lang=args.lang)
    analyzer.fetch_all_data()
    
    # Create output directory
    os.makedirs(args.output, exist_ok=True)
    
    # Generate outputs
    if args.format in ['all', 'summary']:
        print(analyzer.generate_summary())
    
    if args.format in ['all', 'json']:
        import json
        data = {
            'basic': analyzer.analyze_basic_stats(),
            'contributors': analyzer.analyze_contributors(),
            'issues': analyzer.analyze_issues(),
            'pulls': analyzer.analyze_pull_requests(),
            'health': analyzer.calculate_health_score(),
        }
        json_path = os.path.join(args.output, f'{analyzer.repo}_data.json')
        with open(json_path, 'w', encoding='utf-8') as f:
            json.dump(data, f, indent=2, default=str, ensure_ascii=False)
        print(analyzer.t['json_saved'].format(path=json_path))
    
    if args.format in ['all', 'html']:
        analyzer.generate_visualizations(args.output)
        analyzer.generate_html_report(args.output)
    
    print(analyzer.t['analysis_complete'])


